            AND DIRECTOR_POS_TYPE_ID NOT IN (SELECT POS_TYPE_ID FROM RAWWANT.IWANT_SALES_EMP_POSITION_ORG_VIEW)
          GROUP BY DIRECTOR_POS_TYPE_ID) E
    ORDER BY E.POS_TYPE_ID
