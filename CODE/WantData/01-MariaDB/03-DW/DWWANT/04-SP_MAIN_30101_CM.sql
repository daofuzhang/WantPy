          GROUP BY POS_TYPE_ID
          UNION
          SELECT MAX(EDI_NO) AS EDI_NO
               , DIRECTOR_POS_TYPE_ID AS POS_TYPE_ID, MAX(DIRECTOR_POS_TYPE_NAME) AS POS_TYPE_NAME
          FROM RAWWANT.IWANT_SALES_EMP_POSITION_ORG_VIEW
